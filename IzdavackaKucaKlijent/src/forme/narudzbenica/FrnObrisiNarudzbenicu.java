    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      // Narudzbenica narudzbenica=(Narudzbenica) cmbNarudzbenica.getSelectedItem();
        
        int indeks=jtblNarudzbenica.getSelectedRow();
        int brojNarudzebenice=(int) jtblNarudzbenica.getValueAt(indeks,0);
        Narudzbenica narudzbenica=new Narudzbenica();
        narudzbenica.setBrojNarudzbenice(brojNarudzebenice);
                
        KlijentTransferObjekat kto=new KlijentTransferObjekat();
        kto.setPodaci(narudzbenica);
       kto.setOperacija(Konstante.Obrisi_Narudbenicu);
        try {
            ObjectOutputStream out =new ObjectOutputStream(Sesija.dajObjekat().getSoket().getOutputStream());
            out.writeObject(kto);
            
            ObjectInputStream in =new ObjectInputStream(Sesija.dajObjekat().getSoket().getInputStream());
            ServerTransferObjekat sto=(ServerTransferObjekat) in.readObject();
            JOptionPane.showMessageDialog(this, sto.getPodaci());
            NarudzbenicaModelTabeleObrisi nmto=(NarudzbenicaModelTabeleObrisi) jtblNarudzbenica.getModel();
            nmto.obrisiNarudzbenicu(jtblNarudzbenica.getSelectedRow());
        } catch (IOException ex) {
            Logger.getLogger(FrnObrisiNarudzbenicu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrnObrisiNarudzbenicu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

