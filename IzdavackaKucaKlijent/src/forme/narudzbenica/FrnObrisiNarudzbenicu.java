    private void srediTabelu() {
       
        try {
             KlijentTransferObjekat kto=new KlijentTransferObjekat();
        kto.setOperacija(Konstante.Vrati_Narudzbenice);
            ObjectOutputStream out =new ObjectOutputStream(Sesija.dajObjekat().getSoket().getOutputStream());
            out.writeObject(kto);
            
            ObjectInputStream in=new ObjectInputStream(Sesija.dajObjekat().getSoket().getInputStream());
            ServerTransferObjekat sto=(ServerTransferObjekat) in.readObject();
            ArrayList<Narudzbenica> lista=(ArrayList<Narudzbenica>) sto.getPodaci();
            NarudzbenicaModelTabeleObrisi nmto=new NarudzbenicaModelTabeleObrisi(lista);
            jtblNarudzbenica.setModel(nmto);
            for (Narudzbenica narudzbenica : lista) {
                cmbNarudzbenica.addItem(narudzbenica);
            }
           
        } catch (IOException ex) {
            Logger.getLogger(FrnObrisiNarudzbenicu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrnObrisiNarudzbenicu.class.getName()).log(Level.SEVERE, null, ex);
        }
                
    }

